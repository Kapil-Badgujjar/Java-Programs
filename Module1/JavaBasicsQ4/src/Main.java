public class Main {
    public static void main(String[] args) {
        // Declaring variables of different data types
        int i = 50;
        boolean b = true;
        char c = 'A';
        float f = 125.89f;
        double d = 1993820.1009281928921;
        byte x = 127;
        short s = 29019;

        // Printing the values on console
        System.out.println("Value of i (int): " + i);
        System.out.println("Value of b (boolean): " + b);
        System.out.println("Value of c (char): " + c);
        System.out.println("Value of f (float): " + f);
        System.out.println("Value of d (double): " + d);
        System.out.println("Value of x (byte): " + x);
        System.out.println("Value of s (short): " + s);

    }
}